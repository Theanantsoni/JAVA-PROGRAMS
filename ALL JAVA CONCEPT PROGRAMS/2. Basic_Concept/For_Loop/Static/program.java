//For Loop...
//Increment or Decrement
class program
{
	public static void main(String args[])
	{
		System.out.println("Display Increment");
		for(int i=0;i<10;i++)
		{
			System.out.println(i);
		}

		System.out.println("display Decrement");
		for(int j=10;j>0;j--)
		{
			System.out.println(j);
		}
	}
}