//Switch case ... static
class program
{
	public static void main(String args[])
	{
		int no=3;
		switch(no)
		{
		case 1:
			{
				System.out.println("You entered number 1");
				break;
			}
		case 2:
			{
				System.out.println("You entered number 2");
				break;
			}
		case 3:
			{
				System.out.println("You entered number 3");
			}
		}
	}
}